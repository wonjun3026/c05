int	ft_find_next_prime(int nb)
{
	int	i;

	i = 1;
	if (nb < 3)
		return (2);
	while ((long long)i * (long long)i <= (long long)nb)
	{
		if (nb % 2 == 0)
			nb++;
		i += 2;
		if (nb % i == 0)
		{
			nb += 2;
			i = 1;
		}
	}
	return (nb);
}
