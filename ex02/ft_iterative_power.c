int	ft_iterative_power(int nb, int power)
{
	int	i;

	i = 1;
	while (power > 0)
	{
		i *= nb;
		power--;
	}
	if (nb == 0 && power == 0)
		return (1);
	if (power < 0)
		return (1);
	return (i);
}
